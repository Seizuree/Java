package model;

import java.util.ArrayList;

public class Dessert {
	public String name;
	public ArrayList<String> toppings;
	public double price;
	
	public Dessert(String name, ArrayList<String> toppings, double price) {
		super();
		this.name = name;
		this.toppings = toppings;
		this.price = price;
	}
	
	
}	
